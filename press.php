		$(".tabs-navigation__item").eq(0).addClass("tabs-navigation__item--active");
		$(".press__content").eq(0).addClass("press__content--active");
		$(".press__content").eq(0).css("display", "flex");

		$(".tabs-navigation__item").click(function() {
			let active = $(".press__content--active");
			let current = $(this);

			active.removeClass("press__content--active")
			$(".tabs-navigation__item--active").removeClass("tabs-navigation__item--active");

			$(".tabs-navigation__item[data-category='" 
				+ current.attr("data-category") + "']")
				.addClass("tabs-navigation__item--active");

			setTimeout(function() {
				active.hide();
				$(".press__content[data-category='" + current.attr("data-category") + "']")
					.css("display", "flex");
			}, 350);

			setTimeout(function() {
				$(".press__content[data-category='" + current.attr("data-category") + "']")
					.addClass("press__content--active");
			}, 380);
		});
